// Class represents Rational Numbers
public class RatNumber {

 int numer;
 int denom;

 RatNumber(int numer, int denom){
  this.numer = numer;
  this.denom = denom;
 }

 public int getNumer() {
  return this.numer;
 }
 
 public int getDenom() {
  return this.denom;
  
 }
 
 public RatNumber add(RatNumber rn) {
  return rn;
  
 }
 
 public RatNumber subtract(RatNumber rn) {
  return rn;
  
 }
 
 public RatNumber multiply(RatNumber rn) {
  return rn;
  
 }
 
 public RatNumber divide(RatNumber rn) {
  return rn;
  
 }
 
 public RatNumber power(RatNumber rn) {
  return rn;
  
 }
}